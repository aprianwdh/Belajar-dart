void main() {
  var bilangan1 = 12;
  var bilangan2 = 15;
  var operatorr = "-";

  switch (operatorr) {
    case "+":
      print("$bilangan1 + $bilangan2 = ${bilangan1 + bilangan2}");
    case "-":
      print("$bilangan1 - $bilangan2 = ${bilangan1 - bilangan2}");
    case "/":
      print("$bilangan1 / $bilangan2 = ${bilangan1 / bilangan2}");
    case "*":
      print("$bilangan1 * $bilangan2 = ${bilangan1 * bilangan2}");
  }
}

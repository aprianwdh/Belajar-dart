void main() {
  //variabel string
  var hallo = "hallo selamat datang di dart language";
  print(hallo);

  //variabel int
  var umur;
  umur = 19;
  print(umur);

  //variabel double(pecahan)
  var pecahan = 3.14;
  print(pecahan);

  //variabel dynamic (dapat berubah ubah tipe datanya)
  var dynamicc; //variabel dynamic
  dynamicc = 17;
  print(dynamicc);
  print(
      dynamicc = "ini dynamic variabel"); //tipe data berubah dari int ke string
}
